var color = "black";

switch (color) {
    case 'red':
        console.log("Color is Red");
        break;
    case 'yellow':
        console.log("Color is yellow");
        break;
    case 'blue':
        console.log("Color is blue");
        break;
    case 'brown':
        console.log("Color is brown");
        break;
    case 'black':
        console.log("Color is black");
        break;
    default:
        console.log("Color is white");
}